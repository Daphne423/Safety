package com.daphne.safety;

public class Links {
    public String linkp, linko, links, linkr, linkw, linkg;
    public Links(){
    }

    public Links(String linkp, String linko, String links, String linkr, String linkw, String linkg) {
        this.linkp = linkp;
        this.linko = linko;
        this.links = links;
        this.linkr = linkr;
        this.linkw = linkw;
        this.linkg = linkg;
    }

    public String getLinkp() {
        return linkp;
    }

    public void setLinkp(String linkp) {
        this.linkp = linkp;
    }

    public String getLinko() {
        return linko;
    }

    public void setLinko(String linko) {
        this.linko = linko;
    }

    public String getLinks() {
        return links;
    }

    public void setLinks(String links) {
        this.links = links;
    }

    public String getLinkr() {
        return linkr;
    }

    public void setLinkr(String linkr) {
        this.linkr = linkr;
    }

    public String getLinkw() {
        return linkw;
    }

    public void setLinkw(String linkw) {
        this.linkw = linkw;
    }

    public String getLinkg() {
        return linkg;
    }

    public void setLinkg(String linkg) {
        this.linkg = linkg;
    }
}
